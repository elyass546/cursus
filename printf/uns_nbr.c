#include "ft_printf.h"

int	uns_nbr(unsigned int nb)
{
	int	a;

	a = ft_nbrlen(nb);
	if (nb > 9)
		uns_nbr(nb / 10);
	ft_putchar(nb % 10 + '0');
	return (a);
}
