#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	else
	{
		i = ft_strlen(s);
		write(1, s, i);
	}
	return (i);
}
